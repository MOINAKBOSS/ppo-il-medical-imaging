def inject_label_noise(y, flip_fraction=0.05, seed=42):
    np.random.seed(seed)
    n = int(len(y) * flip_fraction)
    indices_to_flip = np.random.choice(len(y), size=n, replace=False)
    y_noisy = y.copy()
    y_noisy[indices_to_flip] = 1 - y_noisy[indices_to_flip]  # Binary flip: 0 → 1, 1 → 0
    return y_noisy

