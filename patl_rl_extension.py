class SampleSelectionEnv(gym.Env):
    def __init__(self, X_unlabeled, y_unlabeled, X_mentor, y_mentor):
        super(SampleSelectionEnv, self).__init__()
        self.X_unlabeled = X_unlabeled
        self.y_unlabeled = y_unlabeled
        self.X_mentor = X_mentor
        self.y_mentor = y_mentor
        self.current_index = 0
        self.model = LogisticRegression(max_iter=1000)

        # Observation: feature vector
        self.observation_space = spaces.Box(low=-np.inf, high=np.inf, shape=(X_unlabeled.shape[1],), dtype=np.float32)

        # Action: 0 = skip, 1 = select
        self.action_space = spaces.Discrete(2)

    def reset(self):
        self.current_index = 0
        self.labeled_X = self.X_mentor.copy()
        self.labeled_y = self.y_mentor.copy()
        return self.X_unlabeled[self.current_index]

    def step(self, action):
        done = False
        reward = 0

        x = self.X_unlabeled[self.current_index]
        y_true = self.y_unlabeled[self.current_index]

        if action == 1:
            # Select the sample: add it to training data
            self.labeled_X = np.vstack([self.labeled_X, x])
            self.labeled_y = np.append(self.labeled_y, y_true)

            # Retrain model and compute reward
            self.model.fit(self.labeled_X, self.labeled_y)
            acc = accuracy_score(self.y_mentor, self.model.predict(self.X_mentor))
            reward = 1 if acc > 0.85 else -1
        else:
            # Skip the sample
            reward = -0.1  # small penalty for not learning

        self.current_index += 1
        if self.current_index >= len(self.X_unlabeled):
            done = True
            obs = self.X_unlabeled[0]  # dummy
        else:
            obs = self.X_unlabeled[self.current_index]

        return obs, reward, done, {}

